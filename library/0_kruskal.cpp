class UnionFind {
public:
	int n;
	vi par; //e
	vi ran; //–Ø‚Ì[‚³
	vi num; //—v‘f”

	UnionFind(int _n) {
		n = _n;
		par.resize(n); ran.resize(n); num.resize(n);
		for (int i = 0; i < n; i++) {
			par[i] = i; ran[i] = 0; num[i] = 1;
		}
	}

	//–Ø‚Ìª‚ð‹‚ß‚é
	int find(int x) {
		if (par[x] == x) {
			return x;
		}
		else {
			return par[x] = find(par[x]);
		}
	}

	//x‚Æy‚Ì‘®‚·‚éW‡‚ð•¹‡
	void unite(int x, int y) {
		x = find(x); y = find(y);
		int numsum = num[x] + num[y];
		if (x == y) {
			return;
		}
		if (ran[x]<ran[y]) {
			par[x] = y;
		}
		else {
			par[y] = x;
			if (ran[x] == ran[y]) {
				ran[x]++;
			}
		}
		num[x] = num[y] = numsum;
	}

	//x‚Æy‚ª“¯‚¶W‡‚É‘®‚·‚é‚©”Û‚©
	bool same(int x, int y) {
		return find(x) == find(y);
	}

};

