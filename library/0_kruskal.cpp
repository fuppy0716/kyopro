ll kruskal(int n) {
	sort(edges.begin(), edges.end());
	UnionFind uf(n);
	ll res = 0;
	for (int i = 0; i < edges.size(); i++) {
		if (!uf.same(edges[i].second.first, edges[i].second.second)) {
			uf.unite(edges[i].second.first, edges[i].second.second);
			res += edges[i].first;
		}
	}
	return res;
}


